const dbConvert = {
  dBuV_to_V: (x) => Math.pow(10, (x - 120) / 20),
  V_to_dBuV: (x) => 20 * Math.log10(x) + 120,
  dBm_to_W: (x) => Math.pow(10, x / 10) / 1000,
  W_to_dBm: (x) => 10 * Math.log10(x * 1000),
  dBuA_to_A: (x) => Math.pow(10, (x - 120) / 20),
  A_to_dBuA: (x) => 20 * Math.log10(x) + 120,
  dBuVm_to_Vm: (x) => Math.pow(10, (x - 120) / 20),
  Vm_to_dBuVm: (x) => 20 * Math.log10(x) + 120,
};

function getCableLoss(type, freqMHz, lengthM) {
  if (!CABLE_DATA[type]) return null;
  const arr = CABLE_DATA[type].loss100;
  let lo = 0, hi = 1;
  for (let i = 0; i < CABLE_FREQS.length - 1; i++) {
    if (freqMHz >= CABLE_FREQS[i] && freqMHz <= CABLE_FREQS[i + 1]) { lo = i; hi = i + 1; break; }
  }
  const t = (freqMHz - CABLE_FREQS[lo]) / (CABLE_FREQS[hi] - CABLE_FREQS[lo]);
  const lossPerHundred = arr[lo] + t * (arr[hi] - arr[lo]);
  const lossPerMeter = lossPerHundred / 100;
  return { total: lossPerMeter * lengthM, perMeter: lossPerMeter };
}

