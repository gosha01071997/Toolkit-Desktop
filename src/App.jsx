// Shared drawing primitives
function DiagBox({ x, y, w, h, label, sub, fill = "#EAF0FD", stroke = "#1E5BE8", textColor = "#1A2233" }) {
  const lines = label.split("\n");
  return (
    <g>
      <rect x={x} y={y} width={w} height={h} rx={6} fill={fill} stroke={stroke} strokeWidth={1.5} />
      {lines.map((l, i) => (
        <text key={i} x={x + w / 2} y={y + h / 2 - (lines.length - 1) * 7 + i * 14} textAnchor="middle" dominantBaseline="central"
          fill={textColor} fontSize={10} fontWeight={700} fontFamily="IBM Plex Sans, sans-serif">{l}</text>
      ))}
      {sub && <text x={x + w / 2} y={y + h - 8} textAnchor="middle" fill="#5A6577" fontSize={8} fontFamily="IBM Plex Sans, sans-serif">{sub}</text>}
    </g>
  );
}
function DiagGnd({ x, y }) {
  return (
    <g>
      <line x1={x} y1={y} x2={x} y2={y + 10} stroke="#5A6577" strokeWidth={1.5} />
      <line x1={x - 10} y1={y + 10} x2={x + 10} y2={y + 10} stroke="#5A6577" strokeWidth={1.5} />
      <line x1={x - 6} y1={y + 14} x2={x + 6} y2={y + 14} stroke="#5A6577" strokeWidth={1.5} />
      <line x1={x - 3} y1={y + 18} x2={x + 3} y2={y + 18} stroke="#5A6577" strokeWidth={1.5} />
    </g>
  );
}
function DiagArrow({ x1, y1, x2, y2, label = "", color = "#1E5BE8", dashed = false }) {
  const dx = x2 - x1, dy = y2 - y1;
  const len = Math.sqrt(dx * dx + dy * dy);
  const ux = dx / len, uy = dy / len;
  const ax = x2 - ux * 8, ay = y2 - uy * 8;
  const px = -uy * 5, py = ux * 5;
  const mid = { x: (x1 + x2) / 2, y: (y1 + y2) / 2 };
  return (
    <g>
      <line x1={x1} y1={y1} x2={x2} y2={y2} stroke={color} strokeWidth={1.5}
        strokeDasharray={dashed ? "4,3" : "none"} />
      <polygon points={`${x2},${y2} ${ax + px},${ay + py} ${ax - px},${ay - py}`} fill={color} />
      {label && <text x={mid.x + 4} y={mid.y - 4} fill={color} fontSize={8} fontFamily="IBM Plex Sans, sans-serif" fontWeight={600}>{label}</text>}
    </g>
  );
}
function DiagLabel({ x, y, text, color = "#5A6577" }) {
  return <text x={x} y={y} fill={color} fontSize={8} fontFamily="IBM Plex Sans, sans-serif" textAnchor="middle">{text}</text>;
}
function DiagWire({ points, color = "#1A2233", dashed = false }) {
  const d = points.map((p, i) => `${i === 0 ? "M" : "L"}${p[0]},${p[1]}`).join(" ");
  return <path d={d} stroke={color} strokeWidth={1.5} fill="none" strokeDasharray={dashed ? "4,3" : "none"} />;
}
