function MiniChart({ dep, activeDs }) {
  const W = 300, H = 140, PL = 42, PR = 20, PT = 12, PB = 32;
  const cw = W - PL - PR, ch = H - PT - PB;

  const allVals = dep.datasets.flatMap(d => d.values);
  const minY = 0, maxY = Math.max(...allVals) * 1.12;
  const xVals = dep.xValues;
  const xLog = xVals.length > 4 && xVals[xVals.length-1] / xVals[0] > 50;
  const toX = (v) => {
    if (xLog) {
      const lo = Math.log10(xVals[0]), hi = Math.log10(xVals[xVals.length-1]);
      return PL + ((Math.log10(v) - lo) / (hi - lo)) * cw;
    }
    return PL + ((v - xVals[0]) / (xVals[xVals.length-1] - xVals[0])) * cw;
  };
  const toY = (v) => PT + ch - ((v - minY) / (maxY - minY)) * ch;

  const xTicks = xVals.filter((_, i) => i % Math.ceil(xVals.length / 5) === 0 || i === xVals.length - 1);
  const yTickCount = 4;
  const yTicks = Array.from({ length: yTickCount + 1 }, (_, i) => minY + (maxY - minY) * i / yTickCount);

  const fmtTick = (v) => v >= 1000 ? (v/1000)+"k" : v >= 100 ? Math.round(v) : v >= 10 ? Math.round(v*10)/10 : Math.round(v*100)/100;

  return (
    <svg width="100%" viewBox={`0 0 ${W} ${H}`} style={{ display: "block", overflow: "visible" }}>
      {/* Grid */}
      {yTicks.map((v, i) => (
        <g key={i}>
          <line x1={PL} y1={toY(v)} x2={PL+cw} y2={toY(v)} stroke="#DDE2EA" strokeWidth={0.8} strokeDasharray={i===0?"none":"3,3"} />
          <text x={PL-4} y={toY(v)+4} textAnchor="end" fill="#8A9BB8" fontSize={8} fontFamily="IBM Plex Sans,sans-serif">{fmtTick(v)}</text>
        </g>
      ))}
      {xTicks.map((v, i) => (
        <g key={i}>
          <line x1={toX(v)} y1={PT} x2={toX(v)} y2={PT+ch} stroke="#EDF0F5" strokeWidth={0.8} />
          <text x={toX(v)} y={PT+ch+12} textAnchor="middle" fill="#8A9BB8" fontSize={8} fontFamily="IBM Plex Sans,sans-serif">{fmtTick(v)}</text>
        </g>
      ))}
      {/* Axes */}
      <line x1={PL} y1={PT} x2={PL} y2={PT+ch} stroke="#BDC3CC" strokeWidth={1} />
      <line x1={PL} y1={PT+ch} x2={PL+cw} y2={PT+ch} stroke="#BDC3CC" strokeWidth={1} />
      {/* Axis labels */}
      <text x={PL + cw/2} y={H-2} textAnchor="middle" fill="#5A6577" fontSize={8} fontFamily="IBM Plex Sans,sans-serif">{dep.xLabel}, {dep.xUnit}</text>
      <text x={8} y={PT+ch/2} textAnchor="middle" fill="#5A6577" fontSize={8} fontFamily="IBM Plex Sans,sans-serif" transform={`rotate(-90,8,${PT+ch/2})`}>{dep.yLabel}, {dep.yUnit}</text>
      {/* Lines */}
      {dep.datasets.map((ds, di) => {
        if (activeDs !== null && activeDs !== di) return null;
        const pts = ds.values.map((v, i) => `${toX(xVals[i])},${toY(v)}`).join(" ");
        const pathD = ds.values.map((v, i) => `${i===0?"M":"L"}${toX(xVals[i])},${toY(v)}`).join(" ");
        return (
          <g key={di}>
            <path d={pathD} stroke={ds.color} strokeWidth={2} fill="none" strokeLinejoin="round" strokeLinecap="round" />
            {ds.values.map((v, i) => (
              <circle key={i} cx={toX(xVals[i])} cy={toY(v)} r={3} fill={ds.color} />
            ))}
          </g>
        );
      })}
    </svg>
  );
}

